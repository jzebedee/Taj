                bs[i] = (byte) (bytesIn[i] ^ Seeded[rc++] ^ lastChar);
                lastChar = (byte) (bytesIn[i] ^ Seeded[rc++]);
            }

            return (utf8Input ? Encoding.GetEncoding("UTF-8") : Encoding.GetEncoding("Windows-1252")).GetString(bs);
